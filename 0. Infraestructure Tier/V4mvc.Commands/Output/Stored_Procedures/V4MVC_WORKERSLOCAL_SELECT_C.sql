CREATE PROCEDURE [dbo].[V4MVC_WORKERSLOCAL_SELECT_C]     
(@IDEMPRESA            NVARCHAR(10),
 @DIVCOD			   NVARCHAR(2),
 @RUT				   NVARCHAR(10) 
) AS     
BEGIN    
 
SELECT TOP 1 AUTOR, VALIDADO 
FROM WORKERSLOCAL 
WHERE EMPRESA = @IDEMPRESA
AND DIVISION = @DIVCOD
AND RUT = @RUT
ORDER BY LOTE
        
END 
