CREATE PROCEDURE [dbo].[V4MVC_CERT_DEUDAS_INSERT_CONTROLLABORAL_CTTA]
(
  @PERIODO		NVARCHAR(6)
 ,@IDEMPRESA	NVARCHAR(10)
 ,@MONTO		INT
 ,@RET_CODE		INT OUTPUT
) AS
BEGIN
	SET @RET_CODE = -1

	IF(EXISTS(
			SELECT
				PERIODO
			FROM CERT_DEUDAS
			WHERE
				EMPRESA = @IDEMPRESA AND
				PERIODO = @PERIODO
	))
		BEGIN
			SET @RET_CODE = 1  /*ERROR LOGICO; YA EXISTE*/
			RETURN
		END
	INSERT INTO CERT_DEUDAS(PERIODO, EMPRESA, MONTO)
	VALUES (@PERIODO, @IDEMPRESA, @MONTO)
END
