CREATE PROCEDURE [dbo].[V4MVC_TRANSPORT_SELECT_BY_PATENTE]
( @PATENTE           nvarchar(8) = NULL) AS
BEGIN

SELECT [PATENTE]
      ,[TIPO]
      ,[COLOR]
      ,[EMPRESA]
      ,[ANOFAB]
      ,[PERMISOCIRC]
      ,[FECSEGURO]
      ,[REVTEC]
      ,[CAPACIDAD]
      ,[OPERINVIER]
      ,[NRO]
      ,[AUTOR]
      ,[MARCA]
      ,[MODELO]
      ,[CODMOP]
      ,[FECHAMOD]
      ,[HORAMOD]
      ,[GRABO]
      ,[FECCHECK]
      ,[FECMINERA]
      ,[LABEL]
      ,[OBS]
      ,[TIENEWISE]
      ,[FUNCACARGO]
      ,[FUNCNOM]
      ,[AREA]
      ,[NUMINT]
      ,[TIPO_PROPIETARIO]
      ,[RUT_PROPIETARIO]
      ,[USUARIO]
      ,[COMBUSTIBLE]
      ,[TRACCION]
      ,[CILINDRADA]
      ,[POTENCIA]
      ,[TURNO]
      ,[SERVICIO]
      ,[MONITOREADO]
      ,[EQUIPOADAS]
  FROM [dbo].[TRANSPORT]
	WHERE (PATENTE = @PATENTE)

END
