CREATE PROCEDURE [dbo].[V4MVC_CLABORAL_ULTIMO_SELECT_CONTROLLABORAL_CTTA]
(
  @ID_DOC    INT
 ,@IDEMPRESA NVARCHAR(10)
)
AS
BEGIN
	SELECT
		SS.EMPRESA,
		SS.PERIODO,
		SS.DIVISION,
		SS.OST,
		SS.CERTUSUARIO,
		SS.CERTFECHA,
		SS.CERTHORA,
		SS.VALIDADO,
		SS.ID
	FROM [dbo].[FN_CLABORAL_ULTIMO](@ID_DOC, @IDEMPRESA) SS
END

