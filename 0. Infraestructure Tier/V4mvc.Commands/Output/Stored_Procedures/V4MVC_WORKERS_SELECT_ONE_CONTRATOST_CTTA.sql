CREATE PROCEDURE [dbo].[V4MVC_WORKERS_SELECT_ONE_CONTRATOST_CTTA]
(
  @RUT	NVARCHAR(10)
) AS 
BEGIN
	SELECT
		TOP 1
		ISNULL(W.NOMBRES, '') AS NOMBRES,
		ISNULL(W.APELLIDOS, '') AS APELLIDOS,
		ISNULL(D.FONO, '') AS FONO,
		ISNULL(D.EMAIL, '') AS EMAIL,
		W.RUT AS RUT
	FROM WORKERS W
	LEFT JOIN DIRECCION D
		ON W.RUT = D.RUT
	WHERE W.RUT = @RUT
END


