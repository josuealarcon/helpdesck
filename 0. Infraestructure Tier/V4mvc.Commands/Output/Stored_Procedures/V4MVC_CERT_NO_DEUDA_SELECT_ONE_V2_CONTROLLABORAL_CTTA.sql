CREATE PROCEDURE [dbo].[V4MVC_CERT_NO_DEUDA_SELECT_ONE_V2_CONTROLLABORAL_CTTA]
(
  @IDEMPRESA	NVARCHAR(10)
 ,@PERIODO		NVARCHAR(6)
 ,@ID			UNIQUEIDENTIFIER
) AS
BEGIN
	SELECT
		CERTUSUARIO,
		CERTFECHA,
		CERTHORA,
		VALIDADO
	FROM CERT_NO_DEUDA
	WHERE
		EMPRESA = @IDEMPRESA AND
		PERIODO = @PERIODO AND
		ID = @ID
END
