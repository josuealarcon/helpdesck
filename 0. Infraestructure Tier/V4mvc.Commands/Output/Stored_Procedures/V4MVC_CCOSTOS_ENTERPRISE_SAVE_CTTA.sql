CREATE PROCEDURE [dbo].[V4MVC_CCOSTOS_ENTERPRISE_SAVE_CTTA]
(
 @EMPRESA		NVARCHAR(10)
,@CCOSTO		NVARCHAR(14)
,@ACTIVO		NVARCHAR(2)
,@DESCRIPCION	NVARCHAR(50)
)
AS
BEGIN

	IF EXISTS	(
		SELECT		CCOSTO 
		FROM		CCOSTOS
		WHERE		CCOSTO			=	@CCOSTO
		AND			EMPRESA			=	@EMPRESA  )
	BEGIN
		UPDATE		CCOSTOS
		SET			ACTIVO			=	@ACTIVO
					,DESCRIPCION	=	@DESCRIPCION
		WHERE		CCOSTO			=	@CCOSTO
		AND			EMPRESA			=	@EMPRESA
	END
	ELSE
	BEGIN
			INSERT INTO CCOSTOS	(	EMPRESA		,CCOSTO		,DESCRIPCION		,ACTIVO	 )
						VALUES	(	@EMPRESA	,@CCOSTO	,@DESCRIPCION		,@ACTIVO )
	END
END
