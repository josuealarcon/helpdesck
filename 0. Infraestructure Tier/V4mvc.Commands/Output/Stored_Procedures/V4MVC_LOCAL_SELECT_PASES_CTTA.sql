CREATE PROCEDURE [dbo].[V4MVC_LOCAL_SELECT_PASES_CTTA]
(
  @DIVCOD  NVARCHAR(20)
) AS
BEGIN
	SELECT
		L.LOCAL,
		L.EMPRESAL,
		L.DETALLE,
		L.LOCALL,
		L.DIVISION,
		L.MANDANTE,
		L.ACTIVO,
		L.P_ACCESO,
		L.FECHAMOD,
		L.HORAMOD,
		0 AS CHECKED,
		0 AS DISABLED
	FROM LOCAL L
	WHERE
		(L.LOCALL = 'ACCESO') AND
		(L.ACTIVO = 'SI') AND
		(L.DIVISION = @DIVCOD)
	ORDER BY L.LOCAL
END
