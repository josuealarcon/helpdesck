CREATE PROCEDURE [dbo].[V4MVC_DOCS_FORMATO_SELECT_EXTENSIONES_CTTA]
(@ID_DOC		INT = NULL ) AS
BEGIN

   SELECT STUFF((
	      SELECT ',' + DOCS_EXT.EXTENSION
		   FROM DOCS_FORMATO, DOCS_EXT 
		   WHERE 
		   (DOCS_FORMATO.ID_DOC = @ID_DOC) AND
		   (DOCS_FORMATO.ID_DOC_EXT = DOCS_EXT.ID_DOC_EXT)
       FOR XML PATH('')
	),1,1, '') AS EXTENSIONES

END