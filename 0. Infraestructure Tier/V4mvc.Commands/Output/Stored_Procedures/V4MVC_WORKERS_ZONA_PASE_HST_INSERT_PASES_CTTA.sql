CREATE PROCEDURE [dbo].[V4MVC_WORKERS_ZONA_PASE_HST_INSERT_PASES_CTTA]
(
  @ID_PASE			INT
 ,@RUT				NVARCHAR(10)
 ,@ID_EMPRESA		NVARCHAR(10)
) AS 
BEGIN
	INSERT INTO WORKERS_ZONA_PASE_HST
	SELECT *
	FROM  WORKERS_ZONA_PASE
	WHERE
		LOTENUM = @ID_PASE AND
		RUT = @RUT AND
		EMPRESA = @ID_EMPRESA
END


