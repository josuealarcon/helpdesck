CREATE PROCEDURE [dbo].[V4MVC_LOTEPASESFUN_SELECT_VALIDA_EXISTENCIA_NLOTE_INFORMECONSULTARAPIDA_CTTA]
(
     @NLOTEPROC INT 
   , @EXISTE BIT  OUTPUT
) AS 
BEGIN

 IF EXISTS(SELECT * FROM LOTEPASES AS L, LOTEPASESFUN AS LF, ADMIN AS A WHERE L.LOTENUM = LF.NLOTEPROC   AND (L.LOTENUM = @NLOTEPROC))
 BEGIN
  SET @EXISTE = 1
 END
 ELSE
 BEGIN
  SET @EXISTE = 0
 END


END
