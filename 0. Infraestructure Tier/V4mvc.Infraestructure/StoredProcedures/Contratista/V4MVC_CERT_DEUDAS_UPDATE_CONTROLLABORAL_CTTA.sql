CREATE PROCEDURE [dbo].[V4MVC_CERT_DEUDAS_UPDATE_CONTROLLABORAL_CTTA]
(
  @PERIODO		NVARCHAR(6)
 ,@IDEMPRESA	NVARCHAR(10)
 ,@MONTO		INT
 ,@RET_CODE		INT OUTPUT
) AS
BEGIN
	SET @RET_CODE = -1

	IF(NOT EXISTS(
			SELECT
				PERIODO
			FROM CERT_DEUDAS
			WHERE
				EMPRESA = @IDEMPRESA AND
				PERIODO = @PERIODO
	))
		BEGIN
			SET @RET_CODE = 2  /*ERROR LOGICO; NO EXISTE*/
			RETURN
		END
	UPDATE CERT_DEUDAS
	SET
		MONTO = @MONTO
	WHERE
		EMPRESA = @IDEMPRESA AND
		PERIODO = @PERIODO
END
