CREATE PROCEDURE [dbo].[V4MVC_POLIZA_MUERTE_UPDATE_V3_CONTROLLABORAL_CTTA]
(
  @ID			UNIQUEIDENTIFIER = NULL
 ,@USUARIO		NVARCHAR(10)
 ,@PERIODO		NVARCHAR(6)
 ,@IDEMPRESA	NVARCHAR(10)
) AS
BEGIN
	DECLARE @CURR_FECHA NVARCHAR(8) = convert(NVARCHAR(8), getdate(), 112) /* yyyymmdd */
	DECLARE @CURR_TIEMPOFULL NVARCHAR(8) = convert(NVARCHAR(8), getdate(), 108) /* hh:mm:ss */
	
	IF(@ID IS NOT NULL)
		BEGIN
			UPDATE POLIZA_MUERTE
			SET
				ID = @ID,
				VALIDADO = 'NO',
				USUARIO = @USUARIO,
				FECHAMOD = @CURR_FECHA,
				HORAMOD = @CURR_TIEMPOFULL
			WHERE
				(EMPRESA = @IDEMPRESA) AND
				(PERIODO = @PERIODO)
		END
	ELSE
		BEGIN
			UPDATE POLIZA_MUERTE
			SET
				VALIDADO = 'NO',
				USUARIO = @USUARIO,
				FECHAMOD = @CURR_FECHA,
				HORAMOD = @CURR_TIEMPOFULL
			WHERE
				(EMPRESA = @IDEMPRESA) AND
				(PERIODO = @PERIODO)
		END
END
