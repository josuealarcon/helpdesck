CREATE PROCEDURE [dbo].[V4MVC_A056_FORM301_UPDATE_V3_CONTROLLABORAL_CTTA]
(
  @ID				UNIQUEIDENTIFIER
 ,@USUARIO			NVARCHAR(10)
 ,@EMPRESA			NVARCHAR(10)
 ,@DIVISION			NVARCHAR(4)
 ,@ID_CERTIFICADO	INT
) AS
BEGIN
	DECLARE @CURR_FECHA NVARCHAR(8) = convert(NVARCHAR(8), getdate(), 112) /* yyyymmdd */
	DECLARE @CURR_TIEMPOFULL NVARCHAR(8) = convert(NVARCHAR(8), getdate(), 108) /* hh:mm:ss */
	
	UPDATE A056_FORM301
	SET
		ID = @ID,
		VALIDADO = 'NO',
		USUARIO = @USUARIO,
		FECHASUBE = @CURR_FECHA,
		HORASUBE = @CURR_TIEMPOFULL
	WHERE
		(EMPRESA = @EMPRESA) AND
		(ID_CERTIFICADO = @ID_CERTIFICADO) AND
		(DIVISION = @DIVISION)
END
