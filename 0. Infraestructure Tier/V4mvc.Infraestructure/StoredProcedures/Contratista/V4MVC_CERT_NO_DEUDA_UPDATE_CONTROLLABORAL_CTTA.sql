CREATE PROCEDURE [dbo].[V4MVC_CERT_NO_DEUDA_UPDATE_CONTROLLABORAL_CTTA]
(
  @PERIODO		NVARCHAR(6)
 ,@IDEMPRESA	NVARCHAR(10)
 ,@OST			NVARCHAR(14)
 ,@RET_CODE		INT OUTPUT
) AS
BEGIN

	SET @RET_CODE = -1

	IF(NOT EXISTS(
			SELECT
				PERIODO
			FROM CERT_NO_DEUDA
			WHERE
				EMPRESA = @IDEMPRESA AND
				PERIODO = @PERIODO AND
				OST = @OST
	))
		BEGIN
			SET @RET_CODE = 2  /*ERROR LOGICO; NO EXISTE*/
			RETURN
		END
END
