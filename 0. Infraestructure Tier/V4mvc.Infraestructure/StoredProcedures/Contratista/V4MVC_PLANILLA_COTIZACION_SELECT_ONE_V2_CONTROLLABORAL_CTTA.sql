CREATE PROCEDURE [dbo].[V4MVC_PLANILLA_COTIZACION_SELECT_ONE_V2_CONTROLLABORAL_CTTA]
(
  @IDEMPRESA	NVARCHAR(10)
 ,@PERIODO		NVARCHAR(6)
 ,@DIVISION		NVARCHAR(4)
 ,@OST			NVARCHAR(20)
) AS
BEGIN
	SELECT
		CERTUSUARIO,
		CERTFECHA,
		CERTHORA,
		VALIDADO,
		EMPRESA,
		DIVISION,
		PERIODO,
		OST
	FROM PLANILLA_COTIZACION
	WHERE
		EMPRESA = @IDEMPRESA AND
		PERIODO = @PERIODO AND
		DIVISION = @DIVISION AND
		OST = @OST
END
