CREATE PROCEDURE [dbo].[V4MVC_POLIZA_MUERTE_INSERT_CONTROLLABORAL_CTTA]
(
  @PERIODO		NVARCHAR(6)
 ,@IDEMPRESA	NVARCHAR(10)
 ,@RET_CODE		INT OUTPUT
) AS
BEGIN

	SET @RET_CODE = -1

	IF(EXISTS(
			SELECT
				PERIODO
			FROM POLIZA_MUERTE
			WHERE
				EMPRESA = @IDEMPRESA AND
				PERIODO = @PERIODO
	))
		BEGIN
			SET @RET_CODE = 1  /*ERROR LOGICO; YA EXISTE*/
			RETURN
		END
	INSERT INTO POLIZA_MUERTE(PERIODO, EMPRESA)
	VALUES (@PERIODO, @IDEMPRESA)
END
