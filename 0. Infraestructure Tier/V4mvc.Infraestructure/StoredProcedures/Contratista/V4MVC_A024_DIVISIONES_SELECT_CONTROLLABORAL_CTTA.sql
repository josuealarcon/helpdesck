CREATE PROCEDURE [dbo].[V4MVC_A024_DIVISIONES_SELECT_CONTROLLABORAL_CTTA]
(
  @IDEMPRESA	NVARCHAR(10)
) AS
BEGIN
	SELECT
		DIVISION,
		DIVCOD
	FROM A024_DIVISIONES
	WHERE DIVCOD IN
					(
						SELECT
							OS.DIVISION
						FROM OSTDIVLOCAL AS OS, OSTSUBC AS OSC, OST
						WHERE
							OSC.EMPRESA = @IDEMPRESA  AND
							OST.NROOST = OS.OST AND
							OSC.OST = OST.NROOST
						GROUP BY OS.DIVISION
					)
END
