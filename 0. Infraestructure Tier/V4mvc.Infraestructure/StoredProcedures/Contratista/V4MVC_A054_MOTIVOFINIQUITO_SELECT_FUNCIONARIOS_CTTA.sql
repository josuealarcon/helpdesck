CREATE PROCEDURE [dbo].[V4MVC_A054_MOTIVOFINIQUITO_SELECT_FUNCIONARIOS_CTTA]
(
  @RUT			NVARCHAR(8) = ''
 ,@DIVCOD		NVARCHAR(4) = ''
 ,@IDEMPRESA	NVARCHAR(11) = ''
)
AS
BEGIN
	DECLARE @ID_DOC INT = 18
	SELECT
		MF.MOTIVO,
		MF.GLOSA,
		MF.ACTIVO,
		[dbo].[V4MVC_FGET_MOTIVO_ULTIMO](MF.MOTIVO, @RUT, @IDEMPRESA, @DIVCOD, @ID_DOC) AS CCHECKED
	FROM A054_MOTIVOFINIQUITO MF
	WHERE MF.ACTIVO = 'SI'
END
